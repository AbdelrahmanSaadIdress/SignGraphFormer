
"""WLASL dataset loader for preprocessed .npy keypoint files.

Expects a JSON split file with the structure:
[
    {"keypoint_path": "data/processed/.../clip.npy",
    "label": "book",
    "label_idx": 5,
    "signer_id": "S007"},
    ...
]
Generated by scripts/download_wlasl.py.
"""

import json
import logging
from pathlib import Path
from typing import List, Dict, Optional, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset

from configs.model_config import ModelConfig
from configs.vocab_config import VocabConfig
from .base_dataset import BaseSignDataset
from preprocessing.augmentation import apply_train_augmentations

logger = logging.getLogger(__name__)


class WLASLDataset(BaseSignDataset):
    """PyTorch Dataset for WLASL preprocessed keypoints.

    Args:
        split_json: Path to the split JSON file (train/val/test).
        cfg: ModelConfig for shape and augmentation parameters.
        vocab: VocabConfig for class-to-index mapping.
        augment: Whether to apply training augmentations.
    """

    def __init__(
        self,
        split_json: str,
        cfg: ModelConfig,
        vocab: VocabConfig,
        augment: bool = False,
    ) -> None:
        self.cfg = cfg
        self.vocab = vocab
        self.augment = augment

        with open(split_json, "r") as f:
            self.samples: List[Dict] = json.load(f)

        # Filter out samples whose .npy files are missing (robust to partial extraction)
        before = len(self.samples)
        self.samples = [s for s in self.samples if Path(s["keypoint_path"]).exists()]
        if len(self.samples) < before:
            logger.warning(
                "Dropped %d samples with missing .npy files (kept %d)",
                before - len(self.samples), len(self.samples)
            )

        logger.info(
            "WLASLDataset loaded: split=%s, samples=%d, augment=%s",
            split_json, len(self.samples), augment
        )

    def __len__(self) -> int:
        """Return number of samples in this split."""
        return len(self.samples)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, int]:
        """Load a keypoint .npy file and return (sequence_tensor, label_idx).

        Args:
            idx: Sample index.

        Returns:
            Tuple of:
                - Float32 tensor of shape (seq_len, input_dim) = (64, 225)
                - Integer class label
        """
        sample = self.samples[idx]
        seq: np.ndarray = np.load(sample["keypoint_path"]).astype(np.float32)

        if self.augment:
            seq = apply_train_augmentations(seq, target_len=self.cfg.seq_len)

        else:
            # Deterministic pad/crop for val/test
            T = seq.shape[0]
            if T != self.cfg.seq_len:
                if T > self.cfg.seq_len:
                    start = (T - self.cfg.seq_len) // 2
                    seq = seq[start : start + self.cfg.seq_len]
                else:
                    pad = np.tile(seq[-1:], (self.cfg.seq_len - T, 1))
                    seq = np.concatenate([seq, pad], axis=0)

        return torch.from_numpy(seq), sample["label_idx"]

    def get_class_weights(self) -> torch.Tensor:
        """Compute inverse-frequency weights for each class.

        Returns:
            Float32 tensor of shape (num_classes,) for weighted loss or sampler.
        """
        counts = torch.zeros(self.vocab.num_classes)
        for s in self.samples:
            counts[s["label_idx"]] += 1
        # Replace zeros with 1 to avoid inf weights for unseen classes
        counts = counts.clamp(min=1)
        weights = 1.0 / counts
        return weights / weights.sum()  # normalize to sum=1


